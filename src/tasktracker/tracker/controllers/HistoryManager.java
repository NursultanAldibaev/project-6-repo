package tracker.controllers;

import tracker.model.Task;

import java.util.List;

/**
 * Интерфейс для менеджера истории.
 * Отвечает за хранение и управление историей просмотров задач.
 */
public interface HistoryManager {
    /**
     * Добавить задачу в историю.
     * Если задача уже присутствует — она перемещается в конец истории
     * (т.е. считается самой последней просмотренной).
     *
     * @param task задача, которую нужно добавить в историю
     */
    void add(Task task);

    /**
     * Удалить задачу из истории по её id.
     * Используется при удалении задачи/эпика/подзадачи из менеджера,
     * чтобы в истории не оставались "мертвые" ссылки.
     *
     * @param id уникальный идентификатор задачи
     */
    void remove(int id);

    /**
     * Получить список всех задач в истории просмотров.
     * История возвращается в порядке добавления (от старых к новым).
     *
     * @return список просмотренных задач
     */
    List<Task> getHistory();
}
